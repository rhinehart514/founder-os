#!/usr/bin/env bash
# gsd-priority-inject.sh — Write product priorities to .claude/rules/ for GSD agents
# Usage: bash bin/gsd-priority-inject.sh [project-dir]
# Refreshed on session start. All GSD agents read .claude/rules/ automatically.
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_DIR="${1:-$(pwd)}"
EVAL_CACHE="$PROJECT_DIR/.claude/cache/eval-cache.json"
FOUNDER_YML="$PROJECT_DIR/config/founder.yml"
RULES_DIR="$PROJECT_DIR/.claude/rules"
OUTPUT="$RULES_DIR/founder-priorities.md"
LEARNINGS=""

# Still run even without eval — vault context is valuable alone
mkdir -p "$RULES_DIR"

# --- Compute bottleneck ---
BOTTLENECK=""
if [[ -f "$SCRIPT_DIR/compute-bottleneck.sh" ]]; then
    BOTTLENECK=$(bash "$SCRIPT_DIR/compute-bottleneck.sh" "$EVAL_CACHE" "$FOUNDER_YML" 2>/dev/null | head -1 || true)
fi

# --- Extract top 3 features ---
TOP3=""
if [[ -f "$SCRIPT_DIR/compute-bottleneck.sh" ]]; then
    TOP3=$(bash "$SCRIPT_DIR/compute-bottleneck.sh" "$EVAL_CACHE" "$FOUNDER_YML" 2>/dev/null | head -3 || true)
fi

# --- Extract relevant learnings ---
for _lf in "$PROJECT_DIR/.claude/knowledge/experiment-learnings.md" "$HOME/.claude/knowledge/experiment-learnings.md"; do
    if [[ -f "$_lf" ]]; then
        # Extract Known Patterns and Dead Ends sections (first 5 lines each)
        KNOWN=$(sed -n '/^## Known/,/^## /p' "$_lf" 2>/dev/null | head -8 | grep -E '^\s*-' || true)
        DEAD=$(sed -n '/^## Dead/,/^## /p' "$_lf" 2>/dev/null | head -8 | grep -E '^\s*-' || true)
        break
    fi
done

# --- Write priorities file ---
{
    echo "## Product Priority Context (auto-generated by founder-os)"
    echo "Updated: $(date +%Y-%m-%d\ %H:%M)"
    echo ""

    if [[ -n "$BOTTLENECK" ]]; then
        IFS=$'\t' read -r bn_name bn_score bn_weight bn_wscore bn_dim <<< "$BOTTLENECK"
        echo "**Bottleneck:** $bn_name at $bn_score/100 (weight: $bn_weight, weakest: $bn_dim)"
        echo ""
    fi

    if [[ -n "$TOP3" ]]; then
        echo "**Priority features (by impact):**"
        echo ""
        echo "| Feature | Score | Weight | Weakest |"
        echo "|---------|-------|--------|---------|"
        while IFS=$'\t' read -r name score weight wscore dim; do
            echo "| $name | $score | $weight | $dim |"
        done <<< "$TOP3"
        echo ""
    fi

    if [[ -n "${KNOWN:-}" ]]; then
        echo "**Known patterns:**"
        echo "$KNOWN"
        echo ""
    fi

    if [[ -n "${DEAD:-}" ]]; then
        echo "**Dead ends (don't repeat):**"
        echo "$DEAD"
        echo ""
    fi

    echo "When making implementation decisions, prefer approaches that improve the bottleneck feature's weakest dimension."

    # --- Pull cross-project intelligence from vault ---
    if [[ -f "$SCRIPT_DIR/vault-query.sh" ]]; then
        # Get project name for scoped search
        PROJ_NAME=""
        [[ -f "$FOUNDER_YML" ]] && PROJ_NAME=$(grep -m1 'name:' "$FOUNDER_YML" 2>/dev/null | sed 's/.*name:\s*//' | xargs || true)
        [[ -z "$PROJ_NAME" ]] && PROJ_NAME=$(basename "$PROJECT_DIR")

        # Pull failed predictions relevant to this project
        PRED_CONTEXT=$(bash "$SCRIPT_DIR/extract-relevant-predictions.sh" "$PROJ_NAME" 2>/dev/null | head -15 || true)
        if [[ -n "$PRED_CONTEXT" ]]; then
            echo ""
            echo "$PRED_CONTEXT"
        fi

        # Pull cross-project learnings (what worked/failed in other projects)
        CROSS=$(bash "$SCRIPT_DIR/vault-query.sh" --cross-project "$PROJ_NAME" 2>/dev/null | head -20 || true)
        if [[ -n "$CROSS" ]]; then
            echo ""
            echo "**Cross-project intelligence:**"
            echo "$CROSS"
        fi
    fi
} > "$OUTPUT"

# --- Write vault router instructions (separate rules file, always written) ---
cat > "$RULES_DIR/vault-knowledge.md" << 'VAULT_EOF'
## Knowledge Vault (auto-injected)

Persistent knowledge lives in ~/obsidian-vault/. Query before researching or planning.

**Lookup commands** (bash, zero tokens):
- `bash bin/vault-query.sh "keywords"` — search all notes
- `bash bin/vault-query.sh --cross-project "keywords"` — patterns from other projects
- `bash bin/vault-query.sh --project-context [name]` — full context for a project

**Structure**: `Knowledge/` (global learnings, predictions, patterns) · `Projects/[name]/` (scores, plans, research) · `Commonplace/` (manual notes)

**Rules**:
- Check vault BEFORE WebSearch — skip the search if vault has it
- Never dump full vault into context — query specific keywords
- Only query at decision points, not every turn
- vault-query returns max 5 notes × 20 lines ≈ 500 tokens
VAULT_EOF
